package ex_10_For_Loop;

public class Lab_96_For_If_Loop {
    public static void main(String[] args) {
        int i;
        for (i=0;i<18;i++)
        {
            if(i<15)
            {
                System.out.println("good");
            }
            else {
                System.out.println("bad");
            }
        }
    }
}
